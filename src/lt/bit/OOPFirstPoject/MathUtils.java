package lt.bit.OOPFirstPoject;

public class MathUtils {
    // Neleis kurti objektu
    private MathUtils(){

    }

    public static int add(int a, int b) {
        return a + b;
    }

    public static int substraction(int a, int b){
        return a - b;
    }

    public static int division(int a, int b){
        return a / b;
    }
}


